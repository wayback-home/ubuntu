def plus(op1, op2):
    result = op1 + op2
    return result


def minus(op1, op2):
    result = op1 - op2
    return result


def multi(op1, op2):
    result = op1 * op2
    return result


def divide(op1, op2):
    result = op1 / op2
    return result


def quotient(op1, op2):
    result = op1 // op2
    return result


def remainder(op1, op2):
    result = op1 % op2
    return result


def square(op1, op2):
    result = op1 ** op2
    return result